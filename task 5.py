sns.pairplot(d, vars=['Age', 'Fare', 'Pclass'], hue='Survived')
plt.show()

"""Passengers in first class (Pclass 1) and those who paid higher fares had better survival chances.

Younger children and first-class adults were more likely to survive than other groups.

Most non-survivors were in third class with lower fares and were typically young adults or middle-aged
"""

numeric_df= d.select_dtypes(include=['number'])
corr_matrix=numeric_df.corr()
plt.figure(figsize=(8,6))
sns.heatmap(corr_matrix, annot=True, cmap='coolwarm')
plt.title('Correlation Matrix')
plt.show()

"""Survival is most strongly linked to class (Pclass) and fare.

Higher class (lower Pclass) and higher fare increase survival chances.

Most other correlations are weak or near zero
"""

sns.boxplot(x='Survived', y='Age', data=d)
plt.title('Age vs Survival')
plt.show()

"""The boxplot shows that the median ages of survivors and non-survivors are similar, but there is a wider age range among survivors, including more children and elderly. Most passengers, regardless of survival, were young to middle-aged adults."""

plt.figure(figsize=(8,6))
sns.scatterplot(x='Age', y='SibSp', hue='Survived', data=d)
plt.title('Scatter Plot: Age vs SibSp (colored by Survived)')
plt.xlabel('Age')
plt.ylabel('Number of Siblings/Spouses Aboard (SibSp)')
plt.show()

"""Most passengers had zero or one sibling/spouse aboard, regardless of age or survival. Survival did not clearly depend on SibSp or age, but families with several siblings/spouses were mainly younger, and included both survivors and non-survivors"""

sns.countplot(x='Pclass', hue='Survived', data=d)
plt.title('Survival Trend by Passenger Class')
plt.show()

"""Survival was highest in first class, moderate in second class, and lowest in third class. Most third-class passengers did not survive, while more first-class passengers survived than died"""



